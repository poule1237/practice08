package com.javaex.ex05;

public class Add {
    //필드
    private int a;
    private int b;
    //생성자
    public Add() {
    	
    }
    
    public Add(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}
    //메소드 gs
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
	//메소드 일반


	
    

}
